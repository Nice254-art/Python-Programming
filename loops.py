# While loop
number = 20
while number <= 25:
    print(number)
    number += 1 # incrementing

    # Decrementing
num = 10
while num >= 1:
    print(num)
    num -= 1

# For loop
languages = ["python", "PHP", "Kotlin", "Java"]
print(languages)
for x in languages:
    print(x)

# Range
for k in range(30, 35):
    print(k)

for y in range(1, 10, 2): # incrementing by 2 the values in the range
    print(y)