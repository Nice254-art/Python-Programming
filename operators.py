# Arithmetic operators
x = 10
y = 4
print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y)  # Modulus - Returns the remainder after dividing two numbers

# Comparison Operators
a = 9
b = 78
print(a < b)
print(a > b)
print(a <= b)
print(a >= b)
print(a == b)  # Egual to
print(a != b)  # Not Egual to

# Assignment Operators
w = 7
print(w)

y = 12
y += 3
print(y)

z = 9
z -= 5
print(z)

# Logical Operators
num1 = 45
num2 = 34
print(num1 < num2 and num2 < num1) # return a true if all conditions are true
print(num1 < num2 or num2 < num1) # returns a true if any of the condition is true
print(not (num1 < num2 or num2 < num1)) # takes the the result and reverses it


